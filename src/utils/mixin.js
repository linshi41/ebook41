import { mapGetters, mapActions } from 'vuex'
import { themeList, addCss, removeAllCss, getReadTimeByMinute } from './book'
import { getBookmark, saveLocation, getBookShelf, saveBookShelf } from './localStorage'
import { gotoBookDetail, appendAddToShelf, computeId, removeAddFromShelf } from './store'
import { shelf } from '../api/store'

export const storeShelfMixin = {
  computed: {
    ...mapGetters([
      'isEditMode',
      'shelfList',
      'shelfSelected',
      'shelfTitleVisible',
      'offsetY',
      'shelfCategory',
      'currentType'
    ])
  },
  methods: {
    ...mapActions([
      'setIsEditMode',
      'setShelfList',
      'setShelfSelected',
      'setShelfTitleVisible',
      'setOffsetY',
      'setShelfCategory',
      'setCurrentType'
    ]),
    showBookDetail(book) {
      gotoBookDetail(this, book)
    },
    getCategoryList(title) {
      this.getShelfList().then(() => {
        // 取出书架中的所有分组,然后选出满足title的分组,filter返回的是一个数组,所以要用[0]
        const categoryList = this.shelfList.filter(book => book.type === 2 && book.title === title)[0]
        this.setShelfCategory(categoryList)
      })
    },
    getShelfList() {
      let shelfList = getBookShelf()
      if (!shelfList) {
        shelf().then(response => {
          if (response.status === 200 && response.data && response.data.bookList) {
            shelfList = appendAddToShelf(response.data.bookList)
            saveBookShelf(shelfList)
            return this.setShelfList(shelfList)
          }
        })
      } else {
        return this.setShelfList(shelfList)
      }
    },
    // 移出分组
    moveOutOfGroup(f) {
      // 对书架的书更新
      this.setShelfList(
          // 对书架上的书进行遍历
          this.shelfList.map(book => {
            if (book.type === 2 && book.itemList) {
              // 对于存有书籍(book.itemList存在)的分组(book.type === 2),保留未被选中的书籍
              book.itemList = book.itemList.filter(subBook => !subBook.selected)
            }
            return book
          })).then(() => {
        // 然后把选中的电子书添加到书架的最后
        //  1.removeAddFromShelf(this.shelfList) 去掉书架上的添加书籍按钮
        //  2.[].concat(removeAddFromShelf(this.shelfList), ...this.shelfSelected) 将之前选中的书籍添加到书架上
        //  3.appendAddToShelf([].concat(removeAddFromShelf(this.shelfList), ...this.shelfSelected))
        //        在书架上把添加书籍按钮加入回来,(先删除再加入从而保证这个按钮放在最后)
        //  4.computeId(appendAddToShelf([].concat( removeAddFromShelf(this.shelfList), ...this.shelfSelected)))
        //        对书架上的书重新计算ID
        const list = computeId(appendAddToShelf([].concat(
            removeAddFromShelf(this.shelfList), ...this.shelfSelected)))
        this.setShelfList(list).then(() => {
          this.simpleToast(this.$t('shelf.moveBookOutSuccess'))
          if (f) f()
        })
      })
    }
  }
}

